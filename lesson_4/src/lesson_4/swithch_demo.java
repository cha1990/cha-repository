package lesson_4;

public class swithch_demo {

	public static void main(String[] args) {

		int x = 10;

		switch (x) {
		case 10:
			System.out.println("ten");
			break;
		case 20:
			System.out.println("twenty");
			break;
		default:
			System.out.println("other");

		}
	}

}
